resumeShown = "false"
topShown = "false"
multimediaShown = "false"

const checkResume = () => {
    if (resumeShown == "false" && topShown == "false") {
      document.querySelector('.viewer').innerHTML = resumeContent;
      document.querySelector('.showResume').innerHTML= "Hide Resume";
      resumeShown = "true";
      topShown = "false";
      return resumeShown;
      return topShown;
    }
    else if (resumeShown == "false" && topShown == "true") {
      document.querySelector('.viewer').innerHTML = resumeHidden;
      document.querySelector('.viewer').innerHTML = resumeContent;
      document.querySelector('.showResume').innerHTML= "Hide Resume";
      document.querySelector('.topArticles').innerHTML= "Show Print Articles";
      resumeShown = "true";
      topShown = "false";
      return resumeShown;
      return topShown;
    }
    else if (resumeShown == "true" && topShown == "false") {
      document.querySelector('.viewer').innerHTML = resumeHidden;
      document.querySelector('.showResume').innerHTML= "Show Resume";
      document.querySelector('.topArticles').innerHTML= "Show Print Articles";
      resumeShown = "false";
      topShown = "false"
      return resumeShown;
      return topShown;
    }
};

const checkTop = () => {
    if (resumeShown == "false" && topShown == "false") {
      document.querySelector('.viewer').innerHTML = topArticles;
      document.querySelector('.topArticles').innerHTML= "Hide Print Articles";
      resumeShown = "false";
      topShown = "true";
      return resumeShown;
      return topShown;
    }
    else if (resumeShown == "false" && topShown == "true") {
      document.querySelector('.viewer').innerHTML = resumeHidden;
      document.querySelector('.topArticles').innerHTML= "Show Print Articles";
      resumeShown = "false";
      topShown = "false";
      return resumeShown;
      return topShown;
    }
    else if (resumeShown == "true" && topShown == "false") {
      document.querySelector('.viewer').innerHTML = resumeHidden;
      document.querySelector('.viewer').innerHTML = topArticles;
      document.querySelector('.topArticles').innerHTML= "Hide Print Articles";
      document.querySelector('.showResume').innerHTML= "Show Resume";
      resumeShown = "false";
      topShown = "true"
      return resumeShown;
      return topShown;
    }
};

document.querySelector('.showResume').onclick = checkResume;
document.querySelector('.topArticles').onclick = checkTop;
